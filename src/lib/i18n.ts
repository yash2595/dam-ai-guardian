const prettifyKey = (key: string) => {
  const last = key.split(".").pop() || key;
  const spaced = last.replace(/_/g, " ").replace(/([a-z])([A-Z])/g, "$1 $2");
  return spaced.replace(/\b\w/g, (char) => char.toUpperCase());
};

